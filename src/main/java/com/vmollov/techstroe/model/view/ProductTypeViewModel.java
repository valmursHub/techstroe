package com.vmollov.techstroe.model.view;

public class ProductTypeViewModel {

    private String id;
    private String name;

    public ProductTypeViewModel() {
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
